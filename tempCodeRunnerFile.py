# Create Array with Random Numbers
import numpy as np

# Create a 1D array of 4 random numbers
var = np.random.rand(4)
print(var)
print("-----")
# Create a 2D array of shape (2, 3) with random numbers
var1 = np.random.rand(2, 3)
print(var1)
print("-----")

# randn()
var2 = np.random.randn(4)
print(var2)
